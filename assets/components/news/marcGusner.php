        <h3>Seit 20 Jahren Kapellmeister</h3>
        <p>20 Jahre die Musikanten motivieren, ihnen die Blasmusik in allen Facetten näher zu bringen,
            mit ihnen für jeden kirchlichen- und weltlichen Anlass etwas einzustudieren, mit ihnen alljährliche Konzerte
            einzuüben,
            die Sommerveranstaltungen für die Gäste zu proben, das zeugt von Liebe zur Musik und zu einem großen
            Vertrauen zu seinen Musikanten.
            Der Dank für seine geleistete Arbeit sagen die Musikanten indem sie seine Dirigate befolgen, damit die
            Zuhörer etwas Wunderbares zu Gehör bekommen.
            Der Dank für seine 20 jährige Kapellmeistertätigkeit wurde ihm in Form einer Urkunde und des Silbernen
            Kapellmeister-Ehrenzeichen mit
            Kranz vom Vorarlberger Blasmusikverband verliehen. Leider konnte er bei den Feierlichkeiten auf dem
            Muttersberg nicht anwesend sein.
            Die Blasmusikbezirksfunktionäre gratulieren Marc auf das Herzlichste zu dieser Auszeichnung.</p>
